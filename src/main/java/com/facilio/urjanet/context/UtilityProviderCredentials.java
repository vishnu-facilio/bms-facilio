package com.facilio.urjanet.context;

public class UtilityProviderCredentials {
	
	String userName;
	String password;
	String upName;
	String upDisplayname;
	String upUrl;
	String templateId;
	public String getTemplateId() {
		return templateId;
	}
	public void setTemplateId(String templateId) {
		this.templateId = templateId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getUpName() {
		return upName;
	}
	public void setUpName(String upName) {
		this.upName = upName;
	}
	public String getUpDisplayname() {
		return upDisplayname;
	}
	public void setUpDisplayname(String upDisplayname) {
		this.upDisplayname = upDisplayname;
	}
	public String getUpUrl() {
		return upUrl;
	}
	public void setUpUrl(String upUrl) {
		this.upUrl = upUrl;
	}
	

}
