-- Facilio Audit Table
 CREATE TABLE IF NOT EXISTS FacilioAudit(
    ID BIGINT PRIMARY KEY AUTO_INCREMENT,
    ORGID BIGINT NOT NULL,
    USER_ID BIGINT NOT NULL,
    APP VARCHAR(200),
    APP_VERSION VARCHAR(20),
    DEVICE_TYPE VARCHAR(50),
    EXCEPTION VARCHAR(1000),
    EXECUTOR VARCHAR(200),
    RESPONSE_SIZE INT,
    RESOPNSE_CODE SMALLINT,
    JOB VARCHAR(500),
    REQ_URI VARCHAR(2000),
    REFERER VARCHAR(5000),
    REMOTE_IP VARCHAR(100),
    TIME_IN_MILLISEC BIGINT(20),
    TIMESTAMP VARCHAR(200),
    TIMETAKEN INT,
    SELECT_QUERY_COUNT BIGINT(20),
    SELECT_QUERY_TIME BIGINT(20),
    INSERT_QUERY_COUNT BIGINT(20),
    INSERT_QUERY_TIME BIGINT(20),
    UPDATE_QUERY_COUNT BIGINT(20),
    UPDATE_QUERY_TIME BIGINT(20),
    DELETE_QUERY_COUNT BIGINT(20),
    DELETE_QUERY_TIME BIGINT(20),
    REDIS_GET_COUNT BIGINT(20),
    REDIS_GET_TIME BIGINT(20),
    REDIS_PUT_COUNT BIGINT(20),
    REDIS_PUT_TIME BIGINT(20),
    REDIS_DELETE_COUNT BIGINT(20),
    REDIS_DELETE_TIME BIGINT(20),
    INSTANT_JOB_COUNT BIGINT(20),
    INSTANT_JOB_FILE_ADDED_TIME BIGINT(20),
    TOTAL_QUERIES BIGINT(20),
    TOTAL_QUERY_TIME BIGINT(20),
    JSON_CONV_TIME BIGINT(20)
 );